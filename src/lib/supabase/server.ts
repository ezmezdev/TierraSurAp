import { createServerClient } from '@supabase/ssr'
import { cookies } from 'next/headers'

// Cliente para Server Components y Route Handlers.
// Lee la sesión desde cookies HTTPOnly — seguro.
export async function createClient() {
  const cookieStore = await cookies()

  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll()
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            )
          } catch {
            // Server Component no puede setear cookies — el middleware lo maneja
          }
        },
      },
    }
  )
}

// Cliente con SERVICE ROLE para operaciones administrativas.
// SOLO usar en API Routes de servidor, NUNCA en el browser.
// Bypasea RLS — usar con extremo cuidado.
